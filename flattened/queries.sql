FROM lineorder
WHERE toYYYYMM(LO_ORDERDATE) = 199401 
  AND LO_DISCOUNT >= 4 AND LO_DISCOUNT <= 6 AND LO_QUANTITY >= 26 AND LO_QUANTITY <= 35

